public class FightReward {
    private int xp;
    private int honor;
    private int heroXp;
    private int gold;

    public int getXp() {
        return xp;
    }

    public void setXp(int xp) {
        this.xp = xp;
    }

    public int getHonor() {
        return honor;
    }

    public void setHonor(int honor) {
        this.honor = honor;
    }

    public int getHeroXp() {
        return heroXp;
    }

    public void setHeroXp(int heroXp) {
        this.heroXp = heroXp;
    }

    public int getGold() {
        return gold;
    }

    public void setGold(int gold) {
        this.gold = gold;
    }
}
